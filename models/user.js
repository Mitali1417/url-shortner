const mongoose = require("mongoose");

const UserScehma = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const USER = mongoose.model("user", UserScehma);
module.exports = USER;
